    private void initCityCmbx() {
       comboCity.removeAllItems();
       int count = 0;
        City[] cities = City.values();
        for (City city_ : cities) {
            comboCity.addItem(cities[count++].toString());
        }
    }
    
    private boolean RegexValidation() {
        
        if(!txtCommunityName.getText().matches("^[a-zA-Z ]+$"))
        {
            txtCommunityName.setBorder(BorderFactory.createLineBorder(Color.RED, 1));
            txtCommunityName.setToolTipText("Please enter a 10 digit number");
            validationCheck=false;
        }
        
        if(txtCommunityName.getText().matches("^[a-zA-Z ]+$"))
        {
            txtCommunityName.setBorder(BorderFactory.createLineBorder(Color.BLUE, 1));
        }
        
        return validationCheck;
    }
    
    private boolean EmpytyFieldValidation() {
        if(txtCommunityName.getText().equals(null) || txtCommunityName.getText().trim().isEmpty() )
        {
            txtCommunityName.setBorder(BorderFactory.createLineBorder(Color.RED, 1));
            txtCommunityName.setToolTipText("This Field Cannot be empty");
            emptyValidationStatus= false;
        }
        if(!txtCommunityName.getText().equals(null) && !txtCommunityName.getText().trim().isEmpty() )
        {
            txtCommunityName.setBorder(BorderFactory.createLineBorder(Color.BLUE, 1));
        }
        
        
        return emptyValidationStatus;
    }

