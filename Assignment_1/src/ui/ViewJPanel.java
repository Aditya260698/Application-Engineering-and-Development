    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblEmployees.getSelectedRow();
        
        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select a row to View.");
            return;
       
    }   
        
       
    DefaultTableModel model = (DefaultTableModel) tblEmployees.getModel();
    Employee selectedEmployee = (Employee) model.getValueAt(selectedRowIndex, 0);
    
    String startDate = null;
    
        try {
            SimpleDateFormat simpleDate = new SimpleDateFormat("MM/dd/yyyy");
            startDate = simpleDate.format(selectedEmployee.getStart_date());
        } catch (Exception ex) {
            
            System.out.print(ex);
        }
    
    txtStart_Date.setText(startDate);
    txtLevel.setText(String.valueOf(selectedEmployee.getLevel()));
    txtTeam_Info.setText(String.valueOf(selectedEmployee.getTeam_info()));
    txtPosition_Title.setText(String.valueOf(selectedEmployee.getPosition_title()));
    txtPhone_Number.setText(String.valueOf(selectedEmployee.getPhone_Number()));
    txtEmail.setText(String.valueOf(selectedEmployee.getEmail()));
    lblPhotoDisplay.setIcon(selectedEmployee.getPic());
    
    
    }//GEN-LAST:event_btnViewActionPerformed


