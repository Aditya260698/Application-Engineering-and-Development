    private void populateTable() {
        
        DefaultTableModel model = (DefaultTableModel) tblEmployees.getModel();
        model.setRowCount(0);
        
        for (Employee es : history.getHistory()){
            
            Object[] row = new Object[4];
            row[0] = es;
            row[1] = es.getName();
            row[2] = es.getAge();
            row[3] = es.getGender();    
            
            model.addRow(row);
        }
    }
