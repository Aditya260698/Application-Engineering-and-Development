    private void btnAgeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgeActionPerformed
        // TODO add your handling code here:
        String age = txtAge.getText();
        ArrayList<Employee> employeeFilteredListByAge = new ArrayList<Employee>();
        for (Employee e: employeelist.getHistory()){
            if(age.equals(String.valueOf(e.getAge()))){
                employeeFilteredListByAge.add(e);
                
            }
        }
        populateEmployeeByFilters(employeeFilteredListByAge);
        txtAge.setText("");
        
        
        
    }//GEN-LAST:event_btnAgeActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblEmployees.getSelectedRow();
        
        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select a row to View.");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblEmployees.getModel();
        Employee selectedEmployee = (Employee) model.getValueAt(selectedRowIndex, 0);
        txtTeamInfo.setText(selectedEmployee.getTeam_info());
        txtPhoneNumber.setText(String.valueOf(selectedEmployee.getPhone_Number()));
        txtEmail.setText(selectedEmployee.getEmail());
        lblDisplayPhoto.setIcon(selectedEmployee.getPic());
        
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = tblEmployees.getSelectedRow();
        
        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select a row to Delete.");
            return;
        } 
        DefaultTableModel model = (DefaultTableModel) tblEmployees.getModel();
        Employee selectedEmployee = (Employee) model.getValueAt(selectedRowIndex, 0);
        
        employeelist.deleteEmployee(selectedEmployee);
        
        JOptionPane.showMessageDialog(this, "Employee deleted successfully");
        populateEmployeeTable();
        
        txtTeamInfo.setText("");
        txtPhoneNumber.setText("");
        txtEmail.setText("");
        lblDisplayPhoto.setIcon(null);
    
        
    }//GEN-LAST:event_btnDeleteActionPerformed

